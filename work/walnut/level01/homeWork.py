def f2():   # 成绩评级
    grade = int(input('请输入成绩: '))
    if 90 < grade <= 100:
        print('A')
    elif 70 < grade <= 90:
        print('B')
    elif 60 < grade <= 70:
        print('C')
    elif 0 <= grade <= 60:
        print('E')
    else:
        print('emmm..., 请保持严肃')


