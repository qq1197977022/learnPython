# 一.数据完整性
#   1.一个数据库即一个完整的业务单元, 可包含多张表, 数据被存储在表中
#   2.表中为了准确存储数据, 保证数据正确有效, 可在创建表的时候, 为表添加一些强制性验证, 包括字段类型, 约束
# 二.字段类型
#   1.数字: int, decimal(定点), float(浮点)
#   2.字符串: char, varchar, text
#   3.日期: datetime
#   4.BIT: bit位
# 三.约束
#   1.主键: primary key
#   2.非空: not null
#   3.唯一: unique
#   4.默认: default
#   5.外键: foreign key


























